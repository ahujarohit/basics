class ArithmeticOperators1 {
	public static void main(String args[]) {
		int a = 5;
		float b = 4;
		float c = 0;
		c = a + b;
		System.out.println(c);			//9.0
		c = a - b;
		System.out.println(c); 			//1.0
		c = a * b;
		System.out.println(c); 			//20.0
		c = a / b;
		System.out.println(c);	 		//1.0
		c = a % b;		
		System.out.println(c);			//1.0
	}
}




















